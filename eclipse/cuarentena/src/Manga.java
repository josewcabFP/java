
public class Manga {
	
	private String color;
	private double largo;
	
	public Manga() {
		
		this.color = "blanco";
		this.largo = 45; 
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public double getLargo() {
		return largo;
	}

	public void setLargo(double largo) {
		this.largo = largo;
	}
	
	

}
